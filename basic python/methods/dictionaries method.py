
# Creating a dictionary
dic = {'key_1': 'value1', 'key_2': 'value2', 35: 'Sadiqhasan Rupani', 37: 'Yasin Bhojani', 33: 'Soham Ganmote'}

# Using operations on the 'dic' variable, which stores dictionaries.

#First we display the dictionary.
print(f'\n The default dic of this program is.\n -> {dic}')

# key method: It returns the dictionary keys.
keys = dic.keys()
print(f'\n Keys of the dictionaries are. \n -> {keys}. ')

# value method: It returns the dictionary values.
values = dic.values()
print(f'\n Values of the dictionaries are. \n -> {values}. ')

# Item method: It returns the dictionary Items.
items = dic.items()
print((f'\n Items of the dictionaries are. \n -> {items}. \n'))